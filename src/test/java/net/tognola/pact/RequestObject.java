package net.tognola.pact;

public class RequestObject {


    private String somethingId;
    private String somethingName;
    private int somethingSize;


    public RequestObject() {
    }



    public String getSomethingId() {
        return somethingId;
    }



    public void setSomethingId(String somethingId) {
        this.somethingId = somethingId;
    }



    public String getSomethingName() {
        return somethingName;
    }



    public void setSomethingName(String somethingName) {
        this.somethingName = somethingName;
    }



    public int getSomethingSize() {
        return somethingSize;
    }



    public void setSomethingSize(int somethingSize) {
        this.somethingSize = somethingSize;
    }






}
